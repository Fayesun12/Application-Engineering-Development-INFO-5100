    private void btnViewAirlinerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewAirlinerActionPerformed
        int selectedRow = tblManageAirliners.getSelectedRow();
        if(selectedRow < 0)
        {
            JOptionPane.showMessageDialog(null,"Please select a Row from table first","warning",JOptionPane.WARNING_MESSAGE);
        }
        else
        {
        Airliner airliner = (Airliner) tblManageAirliners.getValueAt(selectedRow,0);
        ViewAirlinerJPanel viewpanel = new ViewAirlinerJPanel(rightPanel, airliner, airlinedirectory);
        rightPanel.add("ViewAirlinerJPanel",viewpanel);
        CardLayout layout = (CardLayout) rightPanel.getLayout();
        layout.next(rightPanel);
        }
        
    }//GEN-LAST:event_btnViewAirlinerActionPerformed

    private void btnDeleteAirlinerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteAirlinerActionPerformed
        
         int selectedRow = tblManageAirliners.getSelectedRow();
        
        if(selectedRow>=0){
            
            int dialogButton = JOptionPane.YES_NO_OPTION;     
            int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to delete the selected airliner", "Warning", dialogButton);
            
            if(dialogResult == JOptionPane.YES_OPTION){
                
                Airliner a = (Airliner) tblManageAirliners.getValueAt(selectedRow, 0);
                airlinedirectory.deleteAirliner(a);
              
                populate();
            } 
        }
        else{
            JOptionPane.showMessageDialog(null, "Please select a booking history from table", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_btnDeleteAirlinerActionPerformed


