    private void populate() {
        DefaultTableModel dtm = (DefaultTableModel) tblManageAirliners.getModel();
        dtm.setRowCount(0);
        
        for(Airliner a: airlinedirectory.getAirlineList()){
            System.out.println(airlinedirectory.getAirlineList());
            Object[] row = new Object[3];
            row[0] = a;
            row[1] = a.getAirlinerAddress();
            row[2] = a.getTotalFlightsPerDay();
         
            dtm.addRow(row);
        }
    }
