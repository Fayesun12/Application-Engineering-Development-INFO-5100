    private void btnCancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelActionPerformed

          int selectedRow = tblBookingDetails.getSelectedRow();
        
        if(selectedRow>=0){
            
            int dialogButton = JOptionPane.YES_NO_OPTION;     
            int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to delete the selected booking", "Warning", dialogButton);
            
            if(dialogResult == JOptionPane.YES_OPTION){
                
                Customer c = (Customer) tblBookingDetails.getValueAt(selectedRow, 0);
                customerDirectory.deleteCCustomer(c);
              
                populate();
            } 
        }
        else{
            JOptionPane.showMessageDialog(null, "Please select a booking history from table", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        
        
        
    }//GEN-LAST:event_btnCancelActionPerformed

   


