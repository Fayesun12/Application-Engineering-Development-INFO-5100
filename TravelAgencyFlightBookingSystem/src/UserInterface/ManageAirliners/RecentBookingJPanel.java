    private void populate() {
        
        
        DefaultTableModel dtm = (DefaultTableModel)tblBookingDetails.getModel();
        dtm.setRowCount(0);
        for (Customer c: customerDirectory.getCustomerList())
        {
          Object[] row = new Object[11];
          row[0] =c;
          row[1] =c.getCustLastName();
          row[2] =c.getCustPassportNo(); 
          row[3] =c.getCustEmailID();
          row[4] =c.getAirlineName();
          row[5] =c.getCustFlightno();
          row[6] =c.getCustSource();
          row[7] =c.getCustDestination();
          row[8] =c.getCustDepTime();
          row[9] =c.getCustArrTime();
          row[10] =c.getCustSeatNo();

 
          dtm.addRow(row);
        }
   
    }

